"""##### 시계열 데이터 군집화 (df_1_2 & df_2_2)"""

df_1_2['출발일'] = pd.to_datetime(df_1_2['출발일'])
df_1_2['검색일'] = pd.to_datetime(df_1_2['검색일'])
df_2_2['출발일'] = pd.to_datetime(df_2_2['출발일'])
df_2_2['구매일'] = pd.to_datetime(df_2_2['구매일'])

df_1_2['label'] = (df_1_2['출발일'] - df_1_2['검색일']).dt.days
df_2_2['label'] = (df_2_2['출발일'] - df_2_2['구매일']).dt.days

# 항공편 정보(출발일+도착지)
df_1_2['indicator'] = df_1_2['출발일'].astype(str) + "_" + df_1_2['목적지']
df_2_2['indicator'] = df_2_2['출발일'].astype(str) + "_" + df_2_2['목적지']

# pivot 생성
# 미래 기준 (검색일 → 출발일까지)
pivot_future = df_1_2.pivot_table(
    index='indicator',
    columns='label',
    values='가격'
).sort_index(axis=1)

# 과거 기준 (출발일 ← 검색일)
pivot_past = df_2_2.pivot_table(
    index='indicator',
    columns='label',
    values='가격'
).sort_index(axis=1)

# row-wise Z-score 정규화
pivot_future_scaled = pivot_future.apply(lambda row: (row - row.mean()) / row.std(), axis=1)
pivot_past_scaled = pivot_past.apply(lambda row: (row - row.mean()) / row.std(), axis=1)

# 1-2 & 2-2 병합
common_ids = pivot_future_scaled.index.intersection(pivot_past_scaled.index)

merged_scaled = pd.concat([
    pivot_future_scaled.loc[common_ids],
    pivot_past_scaled.loc[common_ids]
], axis=1).dropna()

# 군집화 & 시각화
kmeans = KMeans(n_clusters=4, random_state=42)
merged_scaled['cluster'] = kmeans.fit_predict(merged_scaled)

# 군집별 평균 시계열 시각화
cluster_avg = merged_scaled.groupby('cluster').mean()

plt.figure(figsize=(12, 6))
for c in cluster_avg.index:
    plt.plot(cluster_avg.columns, cluster_avg.loc[c], label=f'Cluster {c}')
plt.title("시계열 군집화 결과 (df_1_2 & df_2_2 정규화 후 병합)")
plt.xlabel("출발일까지 남은 일수 (왼쪽: 미래 df_1_2 | 오른쪽: 과거 df_2_2)")
plt.ylabel("Z-score 정규화된 가격")
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

