def timeslot(time_data):
  if pd.isnull(time_data): return "NULL"
  if 0 <= int(time_data.split(':')[0]) < 6:
    return "새벽"
  elif 6 <= int(time_data.split(':')[0]) < 12:
    return '오전'
  elif 12 <= int(time_data.split(':')[0]) < 18:
    return '오후'
  else:
    return '저녁'

