        stat, p = shapiro(price)
        normal = '정규분포 만족' if p >= 0.05 else '비정규분포'
        print(f"  - {cat}: p = {p:.4f} → {normal}")
        results.append({
            '변수': var,
            '범주': cat,
            '샘플 수': len(price),
            'p-value': round(p, 4),
            '결과': normal
        })

